<script>
import { getStorage, ref as storageRef, getDownloadURL  } from 'firebase/storage'

export default {
  props: ['theme'],
  data() {
    return {
      iniciative: {},
      imageUrl: '',
      feedbacks: [],
      medFeedback: 0.0
    }
  },
  created() {
    this.iniciative = JSON.parse(localStorage.getItem('iniciatives')).find(iniciative => iniciative.theme === this.theme)
    this.feedbacks = this.iniciative.feedback

    let sumFeedback = 0.0
    for (let i = 0; i < this.feedbacks.length; i++) {
      sumFeedback += this.feedbacks[i];
    }
    
    this.medFeedback = sumFeedback / this.feedbacks.length
    if(this.medFeedback > 0 && this.medFeedback <= 20) {
      this.medFeedback = 20
    }
    else if(this.medFeedback > 20 && this.medFeedback <= 40) {
      this.medFeedback = 40
    }
    else if(this.medFeedback > 40 && this.medFeedback <= 60) {
      this.medFeedback = 60
    }
    else if(this.medFeedback > 60 && this.medFeedback <= 80) {
      this.medFeedback = 80
    }
    else if(this.medFeedback > 80 && this.medFeedback <= 100) {
      this.medFeedback = 100
    }

    const storage = getStorage()

          if (this.iniciative.photo) {
            const imageRef = storageRef(storage, 'images/' + this.iniciative.photo)
            getDownloadURL(imageRef)
            .then((url) => {
              console.log('URL da imagem obtida:', url);
              this.imageUrl = url // Retorna a URL da imagem
            })
            .catch((error) => {
              console.error('Erro ao obter URL da imagem:', error)
              throw error; // Lança o erro para ser tratado pelo chamador
            })
          } else {
            const imageRef = storageRef(storage, 'images/' + 'defaultImage.png')
            getDownloadURL(imageRef)
            .then((url) => {
              console.log('URL da imagem obtida:', url);
              this.imageUrl = url // Retorna a URL da imagem
            })
            .catch((error) => {
              console.error('Erro ao obter URL da imagem:', error)
              throw error; // Lança o erro para ser tratado pelo chamador
            })
          }
  }
}
